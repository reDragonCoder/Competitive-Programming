//Castings Examples

#include <iostream>
using namespace std;

int main(){
    int n;
    float f;
    char c;

    //int to char
    cin>>n;
    c=n;
    cout<<c<<endl;

    cout<<endl;

    //char to int
    cin>>c;
    n=c;
    cout<<n<<endl;

    cout<<endl;

    //float to int
    cin>>f;
    n=f;
    cout<<n<<endl;

    return 0;
}